#include "libft.h"

void	ft_putendl_fd(char *s, int fd);

void	ft_putendl_fd(char *s, int fd)
{
	int	len;

	len = ft_strlen((char *)s);
	write (fd, s, len);
	write (fd, "\n", 1);
}
